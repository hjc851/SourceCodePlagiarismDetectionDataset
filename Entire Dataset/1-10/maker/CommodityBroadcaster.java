package maker;

import safekeeping.*;

public class CommodityBroadcaster extends maker.Production {

  public CommodityBroadcaster(double tight, double straddle, Space second, Space pervious) {
    overwrite(tight, straddle, second, pervious);
    this.national = MakerLand.pizza;
  }

  protected synchronized void findAdjacentObjection() throws GarageEvacuateCase {

    try {
      this.rifeCavil = this.formerStoring.closeToken();
    } catch (safekeeping.GarageEvacuateCase e) {
      throw e;
    }
  }

  protected synchronized void goPrevailingObjectiveMousStorehouse() throws ShelvingRepleteExcluded {

    try {
      this.soonWarehouses.tallyConsignment(this.rifeCavil);
      this.rifeCavil = null;
    } catch (safekeeping.ShelvingRepleteExcluded e) {
      throw e;
    }
  }
}
