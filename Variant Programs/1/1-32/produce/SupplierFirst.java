package produce;

import plantCelestial.StorableSubject;
import stored.*;

public class SupplierFirst extends Provider {

  public SupplierFirst(double think, double crop, Caching after) {
    initialisation(think, crop, after, null);
    this.statehood = (CommoditySovereign.practical);
  }

  protected synchronized void inviteLastArtefact() throws StoredVacuousExemption {
    this.rifeCavil = (new StorableSubject());
  }

  protected synchronized void movementLiveArtefactCssStored() throws ShelvingRepleteExcluded {

    try {
      this.comeCloset.attachSection(this.rifeCavil);
      this.rifeCavil = (null);
    } catch (ShelvingRepleteExcluded cma) {
      throw cma;
    }
  }
}
