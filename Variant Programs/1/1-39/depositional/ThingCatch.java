package depositional;

public class ThingCatch {

  public synchronized double sustainThisThing() {
    return this.circulatingPeriods;
  }

  public static synchronized double thisThing() {
    return becomeCombinedOpportunityCarer().sustainThisThing();
  }

  public double circulatingPeriods;
  public static ThingCatch presentedAmountRearing;

  public ThingCatch() {
    this.circulatingPeriods = 0;
  }

  public static synchronized void arrangeYears(double objective) {
    becomeCombinedOpportunityCarer().spikePef(objective);
  }

  public static synchronized ThingCatch becomeCombinedOpportunityCarer() {

    if (presentedAmountRearing == null) presentedAmountRearing = new ThingCatch();

    return presentedAmountRearing;
  }

  public synchronized void spikePef(double benchmark) {
    this.circulatingPeriods = benchmark;
  }
}
