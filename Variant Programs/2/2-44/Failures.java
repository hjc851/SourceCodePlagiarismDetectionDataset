public class Failures {
  public static final int ReplaceableJuncture = 6;
  public Proceedings methods = null;
  public int promptYear = 0;
  public int problemYears = 0;

  public Failures(int culpabilityPeriods, Proceedings proceeding) {
    this.problemYears = (culpabilityPeriods);
    this.promptYear = (culpabilityPeriods + ReplaceableJuncture);
    this.methods = (proceeding);
  }

  public synchronized int becomeAccountableOpportunity() {
    return problemYears;
  }

  public synchronized Proceedings makeMarch() {
    return methods;
  }

  public synchronized int developPoisedHour() {
    return promptYear;
  }
}
