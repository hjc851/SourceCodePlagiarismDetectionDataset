package seng2200;

public class Career {

  public synchronized double canAnotherExtent() {
    return this.unresolvedLong;
  }

  public synchronized seng2200.ProducedStagecoach produceScale() {
    return this.place;
  }

  public synchronized double getduration() {
    return this.timescale;
  }

  public Career(double term, ProducedStagecoach step) {
    this.timescale = term;
    this.unresolvedLong = term;
    this.place = step;
  }

  public synchronized void dateStayTimescale(double lifespan) {
    this.unresolvedLong -= lifespan;
  }

  public double unresolvedLong = 0.0;

  public synchronized void breakTenure(double liveDays) {
    this.place.finisElement(liveDays);
  }

  public double timescale = 0.0;
  public seng2200.ProducedStagecoach place = null;
}
