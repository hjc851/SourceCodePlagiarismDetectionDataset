package produce;

import plantCelestial.StorableSubject;
import stored.*;

public class OperatorConclude extends Provider {
  private CyclicTracedChecklist<StorableSubject> shovel = null;

  public OperatorConclude(double skilled, double pasture, Caching earlier) {
    initialisation(skilled, pasture, null, earlier);
    this.statehood = (CommoditySovereign.throwback);
    this.shovel = (new CyclicTracedChecklist<StorableSubject>());
  }

  protected synchronized void inviteLastArtefact() throws StoredVacuousExemption {

    try {
      this.rifeCavil = (this.recordRepository.firstConsignment());
    } catch (StoredVacuousExemption samad) {
      throw samad;
    }
  }

  protected synchronized void movementLiveArtefactCssStored() {
    this.shovel.pasteUnlikely(this.rifeCavil);
    this.rifeCavil = (null);
  }
}
