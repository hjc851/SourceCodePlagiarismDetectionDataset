package seng2200;

public class Republic {
  public java.lang.String distinguish;
  public double during;

  public Republic() {
    this.during = (0);
    this.distinguish = ("");
  }

  public Republic(String make) {
    this.during = (0);
    this.distinguish = (make);
  }

  public Republic(String surname, double kah) {
    this.during = (kah);
    this.distinguish = (surname);
  }

  public synchronized void expansionRemainder(double theo) {
    this.during += (this.during + theo);
  }

  public synchronized double havePeriod() {
    return this.during;
  }

  public synchronized void dictatedRefer(java.lang.String cite) {
    this.distinguish = (cite);
  }

  public synchronized String toString() {
    return this.distinguish;
  }
}
